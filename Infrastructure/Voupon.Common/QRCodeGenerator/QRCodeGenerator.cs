using System;
using System.Collections.Generic;
using System.Drawing;
using System.IO;
using System.Text;
using ZXing.Presentation;

namespace Voupon.Common.QRGenerator
{
    public class QRCodeGenerator
    {
        public static string GenerateQRCodeToFile(string token,int height,int width,int margin,string fileLoaction)
        {
            var qrCodeWriter = new ZXing.BarcodeWriterPixelData
            {
                Format = ZXing.BarcodeFormat.QR_CODE,
                Options = new ZXing.QrCode.QrCodeEncodingOptions
                {
                    Height = height,
                    Width = width,
                    Margin = margin
                }
            };
            var pixelData = qrCodeWriter.Write(token);       
           
            using (var bitmap = new System.Drawing.Bitmap(pixelData.Width, pixelData.Height, System.Drawing.Imaging.PixelFormat.Format32bppRgb))
            using (var ms = new MemoryStream())
            {
                var bitmapData = bitmap.LockBits(new System.Drawing.Rectangle(0, 0, pixelData.Width, pixelData.Height), System.Drawing.Imaging.ImageLockMode.WriteOnly, System.Drawing.Imaging.PixelFormat.Format32bppRgb);
                try
                {
                    // we assume that the row stride of the bitmap is aligned to 4 byte multiplied by the width of the image    
                    System.Runtime.InteropServices.Marshal.Copy(pixelData.Pixels, 0, bitmapData.Scan0, pixelData.Pixels.Length);
                }
                finally
                {
                    bitmap.UnlockBits(bitmapData);
                }
                // save to stream as PNG at file location   
                bitmap.Save(fileLoaction, System.Drawing.Imaging.ImageFormat.Png);
                // bitmap.Save(@"E:\Download\2c1eeac4ea470bdcd91c\123.png", System.Drawing.Imaging.ImageFormat.Png);           
            }
            return "";
        }

